idade = int(input("Digite a idade de Vitor: "))

if idade >= 18:
    print("Vitor é maior de idade.")
else:
    print("Vitor é menor de idade.")

# Este código verifica se Vitor é maior ou menor de idade com base na variável.
# A variável 'idade' é definida como 18.
# Se a idade for 18 ou mais, ele imprime que Vitor é maior de idade.
# Caso contrário, ele imprime que Vitor é menor de idade.
# Fim do código.
